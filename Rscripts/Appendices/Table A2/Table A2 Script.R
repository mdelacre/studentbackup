simulation=function(n,std){

nSims <- 1000000 #number of simulated experiments (large numbers might take a while)
sd_sample<-numeric(nSims) #set up empty container for all sd estimation

	for(i in 1:nSims){ #for each simulated experiment
  		sim_x<-rnorm(n = n, sd = std) #simulate n participants; as a default option, mean = 0
  		# computing the sample standard deviation
  		sd_sample[i]=sd(sim_x)
  				}
write.table(cbind(n,sd_sample),paste("sd estimation when there are",n,"subjects per groups, where sd=",std,".txt"),sep=";",dec=",")
}

